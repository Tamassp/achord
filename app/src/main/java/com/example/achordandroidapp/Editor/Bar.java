package com.example.achordandroidapp.Editor;

public class Bar {
    private String chord1;
    private String chord2;

    public Bar(String chord1){
        this.chord1 = chord1;
    }

    public String getChord1() {
        return chord1;
    }

    public void setChord1(String chord1) {
        this.chord1 = chord1;
    }

    public String getChord2() {
        return chord2;
    }

    public void setChord2(String chord2) {
        this.chord2 = chord2;
    }
}
